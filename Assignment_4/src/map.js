function drawMap() {
    for (var x = 0; x < 32; x++) {
        for (var y = 0; y < 32; y++) {
            if (g_map[x][y] != 0){
                for (var z = 0; z < g_map[x][y]; z++) {
                    var boxes = new Cube();
                    boxes.color = [0.2, 1.0, 1.0, 1.0];
                    if (g_map[x][y] == C) {
                        boxes.textureNum = -2;
                    } else {
                        boxes.textureNum = 2;
                    }
                    boxes.matrix.translate(0, -.75, 0);
                    boxes.matrix.scale(0.9, 0.9, 0.9);
                    if (g_map[x][y] == C) {
                        boxes.matrix.translate(x-16, 1, y-16);    
                    } else {
                        boxes.matrix.translate(x-16, z, y-16);
                    }
                    boxes.render();
                }
            }
        }
    }
}